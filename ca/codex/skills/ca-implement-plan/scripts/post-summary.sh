#!/usr/bin/env bash
# Post the round-by-round Claude<->Codex exchange as a PR comment.
set -euo pipefail
RUN="${1:?run dir}"; PR="${2:-}"
[ -n "$PR" ] || { echo "[ca] no PR; skipping summary"; exit 0; }
MD="$RUN/exchange-summary.md"
{
  echo "## ca loop — Claude×Codex exchange summary"; echo
  for r in "$RUN"/review-round-*.json; do
    [ -f "$r" ] || continue
    n="$(basename "$r" | tr -dc '0-9')"
    python3 - "$r" "$n" <<'PY'
import json, sys
d = json.load(open(sys.argv[1])); n = sys.argv[2]
print(f"### Round {n} — Claude verdict: **{d.get('verdict','?')}**")
s = (d.get("summary") or "").strip()
if s: print(f"> {s}")
for f in d.get("findings", []):
    flag = "🚫 blocking" if f.get("blocking") else "note"
    print(f"- [{flag}] **{f.get('title','')}** ({f.get('severity','')}) `{f.get('file','')}`")
print()
PY
  done
  echo "_Generated by the ca loop (ca-implement-plan skill)._"
} > "$MD"
gh pr comment "$PR" --body-file "$MD"
echo "[ca] summary posted to $PR"
